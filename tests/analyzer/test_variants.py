def test_variant_diff_v1_v2_reports_added_track(bundles):
    """v1 → v2 adds exactly one track (the drum kit)."""
    delta = variant_diff(bundles["v1"], bundles["v2"])
    added_tracks = [r for r in delta.added_entities if r.type == "TRACK"]
    assert [r.id for r in added_tracks] == ["demo:track-drums"]
    # No track was removed, and no send changed on this step.
    assert not delta.removed_entities
    assert not delta.added_sends


def test_variant_diff_v2_v3_reports_added_send(bundles):
    """v2 → v3 adds the vocal → reverb send."""
    delta = variant_diff(bundles["v2"], bundles["v3"])
    assert len(delta.added_sends) == 1
    send = delta.added_sends[0]
    assert send.type == "CHANNEL_SENDS_TO"
    assert "Reverb Return" in send.label


def test_variant_diff_v2_v3_reports_parameter_change(bundles):
    """v2 → v3 also bumps the vocal EQ Gain, read back with role + owner."""
    delta = variant_diff(bundles["v2"], bundles["v3"])
    assert len(delta.parameter_changes) == 1
    change = delta.parameter_changes[0]
    assert change.role == "GAIN"
    assert change.before_value == 0.5
    assert change.after_value == 0.7
    assert change.processor_id == "demo:proc-eq"
    assert change.channel_id == "demo:track-vox:channel"
