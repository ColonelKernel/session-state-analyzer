def test_build_variant_set_groups_one_family_ordered(bundles):
    """v1/v2/v3 group into one family, ordered v1 < v2 < v3 by ordinal."""
    sets = build_variant_set([bundles["v1"], bundles["v2"], bundles["v3"]])
    assert len(sets) == 1
    variant_set = sets[0]
    assert isinstance(variant_set, VariantSet)
    assert variant_set.family == "demo_song"
    assert variant_set.labels() == ["v1", "v2", "v3"]
    assert [m.ordinal for m in variant_set.members] == [0, 1, 2]
    assert all(isinstance(m, VariantMember) for m in variant_set.members)


def test_build_variant_set_ordinals_from_derived_from_chain(bundles):
    """The ordinal is recovered from the derived-from chain, siblings tie."""
    sets = build_variant_set(list(bundles.values()))
    assert len(sets) == 1
    by_label = {m.label: m for m in sets[0].members}
    assert by_label["v1"].ordinal == 0
    assert by_label["v2"].ordinal == 1
    assert by_label["v2_alpha"].ordinal == 1  # sibling of v2
    assert by_label["v3"].ordinal == 2
    # v2 sorts before its sibling (stable tie-break on snapshot id).
    labels = sets[0].labels()
    assert labels.index("v2") < labels.index("v2_alpha")


def test_build_variant_set_skips_bundles_without_variant(bundles):
    """A bundle carrying no VARIANT entity is not part of any set."""
    from session_explorer.loaders.bundle import load_bundle as _load

    x06 = (
        Path(__file__).resolve().parents[2]
        / "fixtures"
        / "cross-daw"
        / "X06_grouping_depth"
        / "bundles"
        / "synthetic"
    )
    extra = []
    if (x06 / "canonical.snapshot.json").exists():
        extra = [_load(x06)]  # a real bundle with no VARIANT entity
    sets = build_variant_set([bundles["v1"], bundles["v2"], *extra])
    # Only the demo_song family is returned; the VARIANT-less bundle is dropped.
    assert [s.family for s in sets] == ["demo_song"]
    assert {m.label for m in sets[0].members} == {"v1", "v2"}


