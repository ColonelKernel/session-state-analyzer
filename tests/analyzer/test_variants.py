def test_build_variant_graph_derived_from_chain(bundles):
    """DERIVED_FROM runs v1 → v2 → v3 along the composed VARIANT nodes."""
    variant_set = build_variant_set(
        [bundles["v1"], bundles["v2"], bundles["v3"]]
    )[0]
    graph = build_variant_graph(variant_set)

    v1, v2, v3 = (_variant_node(graph, i) for i in (0, 1, 2))
    assert graph.has_edge(v1, v2)
    assert graph.edges[v1, v2]["type"] == "DERIVED_FROM"
    assert graph.has_edge(v2, v3)
    assert graph.edges[v2, v3]["type"] == "DERIVED_FROM"
    # No spurious jump from the root straight to the grandchild.
    assert not graph.has_edge(v1, v3)


def test_build_variant_graph_shares_source_with(bundles):
    """SHARES_SOURCE_WITH links the shared media asset across every member."""
    variant_set = build_variant_set(
        [bundles["v1"], bundles["v2"], bundles["v3"]]
    )[0]
    graph = build_variant_graph(variant_set)

    shares = _edges_of_type(graph, "SHARES_SOURCE_WITH")
    # Three members reference one shared asset ⇒ 3 undirected pairs.
    assert len(shares) == 3
    for u, v in shares:
        assert graph.nodes[u]["type"] == "MEDIA_ASSET"
        assert graph.nodes[v]["type"] == "MEDIA_ASSET"
        assert graph.nodes[u]["entity_id"] == graph.nodes[v]["entity_id"]


def test_build_variant_graph_alternative_of_between_siblings(bundles):
    """ALTERNATIVE_OF joins v2 and its same-ordinal sibling v2_alpha."""
    variant_set = build_variant_set(list(bundles.values()))[0]
    graph = build_variant_graph(variant_set)

    # Members sorted (ordinal, snapshot_id): v1, v2, v2_alpha, v3 ⇒ the two
    # ordinal-1 siblings sit at snapshot_ordinal 1 and 2.
    sib_a, sib_b = _variant_node(graph, 1), _variant_node(graph, 2)
    alt = _edges_of_type(graph, "ALTERNATIVE_OF")
    assert (sib_a, sib_b) in alt or (sib_b, sib_a) in alt
    # A linear pair (root vs grandchild) is never an alternative.
    assert len(alt) == 1


