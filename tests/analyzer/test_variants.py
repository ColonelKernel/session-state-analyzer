"""The variants analyzer (P8): grouping, lineage graph, and version diffing.

Runs against the synthetic ``demo_song`` family under ``fixtures/variants/``
(generated by ``inputs/make_inputs.py`` through the real ``flatten_session`` +
``validate_snapshot`` pipeline, so the frozen bundles cannot drift). The family
is one root (v1), a linear child (v2) and its grandchild (v3), plus an
alternative sibling of v2 (v2_alpha):

* ``build_variant_set`` groups v1/v2/v3 into one family, ordered v1 < v2 < v3;
* ``build_variant_graph`` draws DERIVED_FROM down the chain, SHARES_SOURCE_WITH
  across the shared audio asset, and ALTERNATIVE_OF between the siblings;
* ``variant_diff`` reports the +1 track (v1→v2) and the +1 send and the EQ
  parameter change (v2→v3).
"""

from __future__ import annotations

from pathlib import Path

import pytest

from session_explorer.loaders.bundle import load_bundle
from session_explorer.variants import (
    VariantMember,
    VariantSet,
    build_variant_graph,
    build_variant_set,
    variant_diff,
)

FIXTURES = Path(__file__).resolve().parents[2] / "fixtures" / "variants"
MEMBER_NAMES = ("v1", "v2", "v3", "v2_alpha")


def _require_fixtures() -> None:
    for name in MEMBER_NAMES:
        if not (FIXTURES / name / "canonical.snapshot.json").exists():
            pytest.skip(f"variants fixture {name!r} not generated")


@pytest.fixture(scope="module")
def bundles():
    _require_fixtures()
    return {name: load_bundle(FIXTURES / name) for name in MEMBER_NAMES}


def _variant_node(graph, snapshot_ordinal: int):
    """The composed graph's VARIANT node for the member at position ``i``."""
    for node, data in graph.nodes(data=True):
        if data.get("type") == "VARIANT" and data.get("snapshot_ordinal") == snapshot_ordinal:
            return node
    raise AssertionError(f"no VARIANT node at snapshot_ordinal {snapshot_ordinal}")


def _edges_of_type(graph, rel_type: str):
    return [(u, v) for u, v, d in graph.edges(data=True) if d.get("type") == rel_type]


# ---------------------------------------------------------------------------
# Fixtures validate
# ---------------------------------------------------------------------------


def test_all_fixtures_validate(bundles):
    """Every member bundle re-validates on load under the contract."""
    for name, bundle in bundles.items():
        assert bundle.validation.valid, name
        assert bundle.validation.errors == [], name
        assert bundle.snapshot.entities_of_type("VARIANT"), name


