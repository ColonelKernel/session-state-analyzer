# The two-view bundle
# ---------------------------------------------------------------------------


def _unified_bundle(session: CanonicalSession, graph: Optional[nx.DiGraph]) -> dict:
    warnings = list(session.warnings)
    for descriptor in session.descriptors:
        warnings.extend(descriptor.warnings)

    return {
        "schema_version": CANONICAL_SCHEMA_VERSION,
        "session": to_dict(session),
        "graph": graph_to_dict(graph) if graph is not None else None,
        "descriptors": to_dict(session.descriptors),
        "hidden_state_markers": to_dict(session.hidden_state_markers),
        "recommendations": to_dict(session.recommendations),
        "prov": prov_json(session, graph),
        "warnings": warnings,
        "export_metadata": {
            "dialect": session.dialect,
            "view": "unified",
            "graph_included": graph is not None,
            "native_payload_available": session.native is not None,
        },
    }


def _native_bundle(session: CanonicalSession) -> dict:
    if session.native is None:
        raise ValueError(
            "This session carries no native payload: the driver did not attach "
            "session.native, so a native-view export is impossible. "
            "Use view='unified' instead."
        )
    return {
        "dialect": session.native.dialect,
        "model_name": session.native.model_name,
        "model": to_dict(session.native.model),
    }


def build_bundle(
    session: CanonicalSession,
    graph: Optional[nx.DiGraph] = None,
    view: str = "unified",
) -> dict:
    """Assemble the export bundle in the requested view.

    - ``"unified"``: canonical session dump + graph + descriptors +
      recommendations + hidden-state markers + PROV-O block.
    - ``"native"``: the originating dialect's verbatim native model
      (raises ``ValueError`` when ``session.native`` is absent).
    - ``"both"``: ``{"unified": ..., "native": ...}``.
    """

    if view == "unified":
        return _unified_bundle(session, graph)
    if view == "native":
        return _native_bundle(session)
    if view == "both":
        return {
            "unified": _unified_bundle(session, graph),
            "native": _native_bundle(session),
        }
    raise ValueError(f"Unknown export view {view!r}; expected one of {VIEWS}.")


