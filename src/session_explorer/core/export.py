def _json_default(obj: Any) -> Any:
    """Fallback serialiser for numpy scalars and other stragglers."""

    # numpy scalar types expose .item(); avoid importing numpy here.
    if hasattr(obj, "item") and callable(obj.item):
        try:
            return obj.item()
        except Exception:  # pragma: no cover - defensive
            pass
    if hasattr(obj, "tolist") and callable(obj.tolist):
        return obj.tolist()
    if isinstance(obj, (set, frozenset)):
        return list(obj)
    return str(obj)


def to_json_str(payload: dict, indent: int = 2) -> str:
    """Pretty JSON string with safe defaults for numpy / non-serialisable values."""

    return json.dumps(payload, indent=indent, default=_json_default)


def to_json_bytes(payload: dict, indent: int = 2) -> bytes:
    """UTF-8 encoded JSON, convenient for Streamlit download buttons."""

    return to_json_str(payload, indent=indent).encode("utf-8")


def write_bundle(
    session: CanonicalSession,
    output_dir: Path,
    *,
    graph: Optional[nx.DiGraph] = None,
    view: str = "unified",
    filename: Optional[str] = None,
) -> Path:
    """Build the bundle and write it to ``output_dir`` as pretty-printed JSON.

    Returns the written path. The default filename embeds the session name
    and the view (``my-session_unified_bundle.json``).
    """

    bundle = build_bundle(session, graph=graph, view=view)
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    if filename is None:
        filename = f"{slugify(session.name, 'session')}_{view}_bundle.json"
    output_path = output_dir / filename
    output_path.write_text(to_json_str(bundle), encoding="utf-8")
    return output_path
